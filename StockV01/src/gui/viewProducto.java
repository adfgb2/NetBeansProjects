    public final void verDatosProductos() {

        try {
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

            Controlador c = new Controlador();
            List<Producto> productos = c.traerProductos();

            for (Producto producto : productos) {
                Object datos[] = {producto.getCodigo(), producto.getDescripcion(), producto.getCategoria(), producto.getCantidad(), producto.getCosto(), producto.getPrecio(), producto.getFecing(), producto.getEstado()};
                modelo.addRow(datos);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No hay conexion a la base de datos", "Mensaje", JOptionPane.ERROR_MESSAGE);
        }

    }

    void LimpiarJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        try {
            int a = modelo.getRowCount();
            for (int i = a - 1; i >= 0; i--) {
                modelo.removeRow(i);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error de limpieza de tabla", "Mensaje", JOptionPane.ERROR_MESSAGE);
        }
    }

