    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
        // TODO add your handling code here:
        try {
            Controlador c = new Controlador();
            List<Familia> familias = c.relacion_familia();
            for (Familia familia : familias) {
                this.cboCodigo.addItem(familia.getIdFamilia());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No hay conexion con la base de datos");
        }

    }//GEN-LAST:event_formInternalFrameOpened

    private void cboCodigoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboCodigoItemStateChanged
        // TODO add your handling code here:
        Controlador c = new Controlador();
        Familia fam = c.datos_familia(cboCodigo.getSelectedItem().toString());
        txtDescipcion.setText(String.valueOf(fam.getDescripcion()));
        txtEstado.setText(String.valueOf(fam.getEstado()));
    }//GEN-LAST:event_cboCodigoItemStateChanged

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        try {
            Familia familia = new Familia();
            familia.setIdFamilia(Integer.parseInt(cboCodigo.getSelectedItem().toString()));
            familia.setDescripcion(txtDescipcion.getText());
            familia.setEstado(txtEstado.getText());
            Controlador c = new Controlador();
            c.actualizar_familia(familia);
            JOptionPane.showMessageDialog(null, "Exito");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No hay conexion con la base de datos");
        }

    }//GEN-LAST:event_btnActualizarActionPerformed
