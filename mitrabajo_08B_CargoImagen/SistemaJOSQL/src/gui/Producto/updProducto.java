    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
        // TODO add your handling code here:
        try {
            Controlador a = new Controlador();
            List<Producto> productos = a.relacion_producto();
            for (Producto producto : productos) {
                this.cboCodigo.addItem(producto.getIdProducto());
            }
            Controlador b = new Controlador();
            List<Familia> familias = b.relacion_familia();
            for (Familia familia : familias) {
                this.cboCfamilia.addItem(familia.getIdFamilia());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No hay conexion con la base de datos");
        }

    }//GEN-LAST:event_formInternalFrameOpened

    private void cboCodigoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboCodigoItemStateChanged
        // TODO add your handling code here:
        Controlador c = new Controlador();
        Producto proc = c.datos_producto(cboCodigo.getSelectedItem().toString());
        txtDescripcion.setText(String.valueOf(proc.getDescripcion()));        
        txtFregistro.setText(String.valueOf(proc.getFechaDeRegistro()));
        txtFbaja.setText(String.valueOf(proc.getFechaDeBaja()));
        txtEstado.setText(String.valueOf(proc.getEstado()));
        txtFoto.setText(String.valueOf(proc.getFoto()));
    }//GEN-LAST:event_cboCodigoItemStateChanged
