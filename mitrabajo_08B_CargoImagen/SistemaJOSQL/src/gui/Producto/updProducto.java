    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        try {
            Producto prod = new Producto();
            prod.setIdProducto(Integer.parseInt(cboCodigo.getSelectedItem().toString()));
            prod.setDescripcion(txtDescripcion.getText());
            Familia myfamilia = new Familia();
            myfamilia.setIdFamilia(Integer.parseInt(cboCfamilia.getSelectedItem().toString()));
            prod.setFamilia(myfamilia);
            SimpleDateFormat fecha = new SimpleDateFormat("dd-MM-yy");
            SimpleDateFormat fechaa = new SimpleDateFormat("dd-MM-yy");
            prod.setFechaDeRegistro(fecha.parse(txtFregistro.getText()));
            prod.setFechaDeBaja(fechaa.parse(txtFbaja.getText()));
            prod.setEstado(txtEstado.getText());
            //prod.setFoto(txtFoto.getText());
            Controlador c = new Controlador();
            c.actualizar_producto(prod);
            JOptionPane.showMessageDialog(null, "Exito");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No hay conexion con la base de datos");
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

