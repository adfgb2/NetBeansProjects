    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
        // TODO add your handling code here:
        try {
            Controlador c = new Controlador();
            List<Proveedor> proveedores = c.relacion_proveedor();
            for (Proveedor proveedor : proveedores) {
                this.cboProveedor.addItem(proveedor.getIdProveedor());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No hay conexion con la base de datos");
        }
    }//GEN-LAST:event_formInternalFrameOpened

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        try {
            Ingreso xing = new Ingreso();
            xing.setTipoIngreso(cboTipo.getSelectedItem().toString());
            xing.setTipoDoc(cboDoc.getSelectedItem().toString());
            xing.setSerieDocIngreso(txtSerie.getText());
            xing.setNumeroDoc(txtNumero.getText());
            SimpleDateFormat fecha = new SimpleDateFormat("yy-MM-dd");
            xing.setFechaIngreso(fecha.parse(txtFingreso.getText()));
            Proveedor prov = new Proveedor();
            prov.setIdProveedor(cboProveedor.getSelectedItem().toString());
            xing.setProveedor(prov);
            Controlador c = new Controlador();
            c.insertar_ingreso(xing);
            JOptionPane.showMessageDialog(null, "Exito");
            txtSerie.setText("");
            txtNumero.setText("");
            txtFingreso.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No hay conexion con la base de datos");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed
