    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:        
        String u = txtUsuario.getText();
        String p = new String(txtPassword.getPassword());
        Controlador c = new Controlador();        
        if (u.equals("ADMIN") || u.equals("admin") && c.verificarUsusario(u, p)) {
            Mantenimiento m = new Mantenimiento();
            m.setVisible(true);
            dispose();
        }else if(c.verificarUsusario(u, p)){
            OtrosUsuarios o = new OtrosUsuarios();
            o.setVisible(true);
            dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Usuario o contrase√±a incorrecta", "Mensaje", JOptionPane.ERROR_MESSAGE);
            txtUsuario.setText("");
            txtPassword.setText("");
        }

    }//GEN-LAST:event_btnIngresarActionPerformed

