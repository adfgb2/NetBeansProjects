    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        try {
            Producto p = new Producto();
            p.setCodigoProducto(Integer.parseInt(txtCodigo.getText()));
            p.setPresentacion(txtPresentacion.getText());
            Controlador c = new Controlador();
            c.insertarProducto(p);
            JOptionPane.showMessageDialog(rootPane, "Se agrego producto con exito");
            txtCodigo.setText("");
            txtPresentacion.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No hay conexion a la base de datos", "Mensaje", JOptionPane.ERROR_MESSAGE);
        }
}//GEN-LAST:event_btnAgregarActionPerformed
