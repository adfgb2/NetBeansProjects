    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        try {
            Usuario u = new Usuario();
            String obtCodigo = cboUsuario.getSelectedItem().toString();
            u.setCodigoUsuario(Integer.parseInt(obtCodigo.substring(0, 1)));
            u.setClave(new String(txtClave.getPassword()));
            u.setNombre(txtNombre.getText());
            u.setApellido(txtApellido.getText());
            u.setEmail(txtEmail.getText());
            Controlador c = new Controlador();
            c.modificarUsuario(u);
            JOptionPane.showMessageDialog(rootPane, "Se actualizo usuario con exito");
            txtClave.setText("");
            txtNombre.setText("");
            txtApellido.setText("");
            txtEmail.setText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No hay conexion a la base de datos", "Mensaje", JOptionPane.ERROR_MESSAGE);
        }
}//GEN-LAST:event_btnAgregarActionPerformed

