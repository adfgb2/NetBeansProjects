    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        try {
            Controlador c = new Controlador();
            List<Usuario> usuarios = c.mostrarUsusarios();
            for (Usuario usuario : usuarios) {
                this.cboUsuario.addItem(usuario.getCodigoUsuario() + " - " + usuario.getUsuario());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No hay conexion a la base de datos", "Mensaje", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_formWindowOpened

    private void cboUsuarioItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboUsuarioItemStateChanged
        // TODO add your handling code here:
        Controlador c = new Controlador();
        String obtCodigo = cboUsuario.getSelectedItem().toString();
        Usuario u = c.datosUsuario(obtCodigo.substring(0, 1));
        txtClave.setText(u.getClave());
        txtNombre.setText(u.getNombre());
        txtApellido.setText(u.getApellido());
        txtEmail.setText(u.getEmail());
    }//GEN-LAST:event_cboUsuarioItemStateChanged
